package com.example.file_uploader_backend.domain;

import javax.persistence.*;

@Entity
public class Picture {
    @Id
    private String id;
    private String name;
    private String type;
    @Lob
    private byte[] data;

    public Picture() {
    }

    public Picture(String id, String name, String type, byte[] data) {
        this.id = id;
        this.name = name;
        this.type = type;
        this.data = data;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public byte[] getData() {
        return data;
    }

    public void setData(byte[] data) {
        this.data = data;
    }
}
